#define N 24

bool	solve(int maze[N][N], int visited[N][N], int row, int col)
{		
	if (row < 0 || row >= N || col < 0 || col >= N || !maze[row][col] || visited[row][col])
		return (false);
	visited[row][col] = 1;
	if (maze[row][col] == 2)
		return (true);
	if (solve(maze, visited, row + 1, col))
			return (true);
	if (solve(maze, visited, row - 1, col))
			return (true);
	if (solve(maze, visited, row, col + 1))
			return (true);
	if (solve(maze, visited, row, col - 1))
			return (true);
	visited[row][col] = 0;
	return (false);
}

